CREATE TABLE cliente (

    id SERIAL,
    cpf VARCHAR(20),
    nome VARCHAR(100),
    idade INT,
    sexo VARCHAR(10),
    endereco VARCHAR(200),
    telefone VARCHAR(20),
    email VARCHAR(100),
    profissao VARCHAR(100),
    renda DOUBLE,
    estado_civil VARCHAR(20),
    nivel_educacional VARCHAR(50)
    
);



